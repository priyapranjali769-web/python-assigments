#create dictionary 
dictionary = {'name': 'Riya', 'age':18, 'city':'pune'}
print(dictionary)
#accessing dictionary elements
print("accessing name from dictionary :", dictionary['name'])
#adding an element to dictionary 
dictionary['country'] = 'India'
print("dictionary after adding country :", dictionary)
#removing an element from dictionary 
dictionary.pop('age')
print("dictionary after removing age :", dictionary)
#updating an element in dictionary
dictionary['age'] = 19 
print("dictionary after updating age :", dictionary)
#keys of dictionary
keys = dictionary.keys()
print("keys of dictionary :", keys)
#removing specific key
dictionary.pop('city')
print("dictionary after removing city :", dictionary)
